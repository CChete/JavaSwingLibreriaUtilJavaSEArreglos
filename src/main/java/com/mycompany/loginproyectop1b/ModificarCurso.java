    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String fechaInicio = FechaInicio.getText();
        String fechaFin = FechaFin.getText();//estas variables tienen los datos de los jTextField para agregarlos a los datos del curso 
        String profesor1 = jComboBox1.getSelectedItem().toString();//agregamos una variable que contendra el valor del profesor seleccionado el combobox
       
       

       
        modificarCurso.setId(Integer.parseInt(jTextField1.getText()));//se modifican todos los datos del curso con los datos actuales de los jTextField
        modificarCurso.setNombre(jTextField2.getText());
        modificarCurso.setSeccion(jTextField3.getText());
        modificarCurso.setFechainicio(fechaInicio);
        modificarCurso.setFechaFin(fechaFin);
        modificarCurso.setHoraInicio(jTextField10.getText());
        modificarCurso.setHoraFin(jTextField11.getText());
        modificarCurso.Profesor= jComboBox1.getSelectedItem().toString();//se agrega el nombre del profesor a la variable con el combobox
       
       

        profesor profesorSeleccionado = null;// se ingresa un profesor que contendra el nuevo profesor del curso
        Curso cursoSeleccionado = null; //se ingresa un curso que contendra el curso que fue actualizado
        
        for(profesor e : LoginProyectoP1B.profesor){// for que ubica el profesor con el dato del combobox
            if(e.getNombre().equals(profesor1)){
                profesorSeleccionado = e;
                
                break;
            }

        }
          for(Curso f : LoginProyectoP1B.cursos){//for que ubica el curso con el nombre del curso modificado
              
               if(f.getNombre().equals(modificarCurso.getNombre())){
                cursoSeleccionado = f;
                break;
            } 
              
          }
          
         if(profesorSeleccionado!=null && cursoSeleccionado!=null){// si el curso y profesor fue ubicado se procede a agregarlos ya que fue borrado previamente el profesor anterior
            profesorSeleccionado.cursos.add(cursoSeleccionado);//agregamos el curso modificado a el array de cursos del profesor
            cursoSeleccionado.profesor.add(profesorSeleccionado);//agregamos el profesor nuevo a el array de cursos
            JOptionPane.showMessageDialog(this, "curso Actualizado exitosamente");// mensaje que indica que todo fue realizado con exito
        }
      

 
    }//GEN-LAST:event_jButton1ActionPerformed

   

