    public ModificarCurso(int posicion) {
        initComponents();
        
        modificarCurso = LoginProyectoP1B.cursos.get(posicion);// utiliza el constructor con la posicion seleccionada para ubicar el curso a modificar
       
         for(profesor e : LoginProyectoP1B.profesor){//con este for se llena el combobox de profesores con el nombre
            jComboBox1.addItem(e.getUsuario());
            }
         
        jTextField1.setText(String.valueOf(modificarCurso.getId()));// se setean cada uno de los datos del curso a modificar en los jTextField
        jTextField2.setText(modificarCurso.getNombre());
        jTextField3.setText(modificarCurso.getSeccion());
        FechaInicio.setText(modificarCurso.getFechainicio());
        FechaFin.setText(modificarCurso.getFechaFin());
        jTextField10.setText(modificarCurso.getHoraInicio());
        jTextField11.setText(modificarCurso.getHoraFin());
      
        jComboBox1.setSelectedItem(modificarCurso.Profesor);// esta linea coloca el combobox con el dato del profesor asignado al curso
        
        String ProfesorAnt = jComboBox1.getSelectedItem().toString() ;//agrego una variable string para el valor actual de profesor seleccionado en el combobox
        
        profesor profesorSeleccionado = null;// ingreso un profesor que almacenara el profesor seleccionado en el curso
        Curso cursoSeleccionado = null;//ingreso un curso que contendra el curso seleccionado actualmente
        
          for(profesor e : LoginProyectoP1B.profesor){//este for recorre el arraylist de profesores para ubicar el profesor actual del curso
            if(e.getNombre().equals(ProfesorAnt)){//en el if se compara el valor del nombre de los profesores y la variable ProfesorAnt
                profesorSeleccionado = e;//se ubica el profesor
                break;
            }        
        
       }
          for(Curso f : LoginProyectoP1B.cursos){ //este for ubica el curso que estamos modificando en el arraylist de cursos
              
               if(f.getNombre().equals(modificarCurso.getNombre())){//con este if se compara el nombre de modificarCurso con los cursos para ubicarlo
                cursoSeleccionado = f;//se ubica el curso
                break;
            } 
              
          }
            if(profesorSeleccionado!=null && cursoSeleccionado!=null){//si hay un curso y profesor ubicados entonces ejecute
            profesorSeleccionado.cursos.remove(cursoSeleccionado);//borramos el curso agregado  previamente al profesor para poder modificarlo
            cursoSeleccionado.profesor.remove(profesorSeleccionado);//borramos el profesor previamente asignado al curso para poder modificarlo
        }
        
        
        
    }

