    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String fechaInicio = DiaInicio.getText() + "-"+MesInicio.getText()+"-"+aInicio.getText();
        String fechaFin = DiaFin.getText()+"-"+MesFin.getText()+"-"+aFin.getText();
        String profesor1 = jComboBox1.getSelectedItem().toString();
        
     
        
        Curso c = new Curso();
        c.setId(Integer.parseInt(jTextField1.getText()));
        c.setNombre(jTextField2.getText());
        c.setSeccion(jTextField3.getText());
        c.setFechainicio(fechaInicio);
        c.setFechaFin(fechaFin);
        c.setHoraInicio(jTextField10.getText());
        c.setHoraFin(jTextField11.getText());
        c.Profesor = profesor1;
        LoginProyectoP1B.cursos.add(c);
        
        profesor profesorSeleccionado = null;
        Curso cursoSeleccionado = null;
        
          for(profesor e : LoginProyectoP1B.profesor){
            if(e.getNombre().equals(profesor1)){
                profesorSeleccionado = e;
                break;
            }        
        
       }
          for(Curso f : LoginProyectoP1B.cursos){
              
               if(f.getNombre().equals(c.getNombre())){
                cursoSeleccionado = f;
                break;
            } 
              
          }
          
         if(profesorSeleccionado!=null && cursoSeleccionado!=null){
            profesorSeleccionado.cursos.add(cursoSeleccionado);
            cursoSeleccionado.profesor.add(profesorSeleccionado);
            JOptionPane.showMessageDialog(this, "curso creado exitosamente");
        }   
    }//GEN-LAST:event_jButton1ActionPerformed

